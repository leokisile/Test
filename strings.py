nombre = " ada lovelace "

print(nombre.title()) #Da formato de mayusculs despues de cada espacio
print(nombre.upper()) #Pone todo en mayusculas
print(nombre.lower()) #Pone todo en minusculas

print(nombre.rstrip())#Elimina el espacio a la darecha
print(nombre.lstrip())#Elimina el espacio a la izquierda
print(nombre.strip())#Elimina ambos espacios